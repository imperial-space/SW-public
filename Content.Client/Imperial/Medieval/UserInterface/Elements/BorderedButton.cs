using Robust.Client.AutoGenerated;
using Robust.Client.UserInterface;
using Robust.Client.UserInterface.Controls;
using Robust.Client.UserInterface.XAML;

namespace Content.Client.Imperial.Medieval.UserInterface.Elements;

[GenerateTypedNameReferences]
public sealed partial class BorderedButton : Control
{
    public string? Text
    {
        get => Label.Text;
        set => Label.Text = value;
    }

    public Color TextColor
    {
        get => Label.Modulate;
        set => Label.Modulate = value;
    }

    public Color BackgroundColor
    {
        get => Panel.BackgroundPanelColor;
        set => Panel.BackgroundPanelColor = value;
    }

    public float BorderRadius
    {
        get => Panel.BorderRadius;
        set => Panel.BorderRadius = value;
    }

    public Button Button => MainButton;

    public BorderedButton()
    {
        RobustXamlLoader.Load(this);

        // Set the default text color to white
        Label.Modulate = Color.White;
        Panel.ModulateSelfOverride = Color.White;
    }

    public void SetText(string text)
    {
        Label.Text = text;
    }

    public void SetColor(Color color)
    {
        Panel.BackgroundPanelColor = color;
    }
}
