using Content.Client.Imperial.UI.Windows;
using Content.Shared.Imperial.Medieval.Exam.Prototypes;
using Robust.Client.AutoGenerated;
using Robust.Client.UserInterface.XAML;
using Robust.Shared.Prototypes;
using Robust.Shared.Utility;

namespace Content.Client.Imperial.Medieval.Exam.UI;

[GenerateTypedNameReferences]
public sealed partial class ExamResultWindow : BaseImperialWindow
{
    private const string TitleSuccess = "Успех!";
    private const string TitleFail = "Подготовьтесь лучше.";
    private const string TextSuccess = """
    Поздравляем, тестирование успешно пройдено!
    Профессия “Глава службы безопасности” успешно разблокирована.
    """;
    private const string TextFail = """
    К сожалению, вы допустили больше двух ошибок, и тестирование было не пройдено.
    Не отчаивайтесь: вы сможете попробовать сдать тест повторно через 24 часа.

    За это время рекомендуем подробно ознакомиться с wiki.imperialspace.net, так
    вы сможете подойти к тестированию с максимальной подготовкой. Не стесняйтесь
    спросить совета у более опытных игроков в игре: вам обязательно помогут.
    """;

    [Dependency] private readonly IPrototypeManager _prototype = default!;

    public ExamResultWindow()
    {
        RobustXamlLoader.Load(this);
        IoCManager.InjectDependencies(this);
    }

    public void Refresh(string prototype, int incorrect, bool passed)
    {
        if (!_prototype.TryIndex<ExamPrototype>(prototype, out var exam))
            return;

        var message = new FormattedMessage();

        message.TryAddMarkup(passed ? TitleSuccess : TitleFail, out _);
        LabelTitle.SetMessage(message);

        message = new FormattedMessage();
        message.TryAddMarkup(passed ? TextSuccess : TextFail, out _);
        LabelDescription.SetMessage(message);

        if (exam.IconPath != ResPath.Empty)
            Icon.TexturePath = exam.IconPath.ToString();
    }
}
