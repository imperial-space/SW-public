using Content.Client.Imperial.UI.Windows;
using Content.Shared.Imperial.Medieval.Exam.Prototypes;
using Robust.Client.AutoGenerated;
using Robust.Client.UserInterface.Controls;
using Robust.Client.UserInterface.XAML;
using Robust.Shared.Prototypes;
using Robust.Shared.Random;
using Robust.Shared.Utility;

namespace Content.Client.Imperial.Medieval.Exam.UI;

[GenerateTypedNameReferences]
public sealed partial class ExamWindow : BaseImperialWindow
{
    [Dependency] private readonly IRobustRandom _random = default!;

    public event Action<ProtoId<ExamPrototype>, IReadOnlyDictionary<string, int>>? Sent;

    private readonly Dictionary<string, int> _answers = new();
    private ProtoId<ExamPrototype>? _exam;

    private int _answerCount;
    private bool _sent;

    public ExamWindow()
    {
        RobustXamlLoader.Load(this);
        IoCManager.InjectDependencies(this);

        SetExam(null);

        SendButton.OnPressed += OnSendPress;
        SendButton.Label.FontColorOverride = Color.FromHex("#454545");
    }

    protected override void Opened()
    {
        base.Opened();

        _answers.Clear();
        _sent = false;
    }

    public override void Close()
    {
        base.Close();

        Send();
        SetExam(null);
    }

    public void SetExam(ExamPrototype? exam)
    {
        // Menu state
        ExamsContainer.Visible = exam is not null;

        if (exam is null)
            return;

        _exam = exam.ID;

        // Name
        var message = new FormattedMessage();
        message.TryAddMarkup(Loc.GetString(exam.Name), out _);
        ExamName.SetMessage(message);

        // Icon
        if (exam.IconPath != ResPath.Empty)
            ExamIcon.TexturePath = exam.IconPath.ToString();

        // Answers
        TaskContainers.Children.Clear();

        _answerCount = exam.Selection;

        var tasks = new List<ProtoId<ExamTaskPrototype>>(exam.Tasks);
        _random.Shuffle(tasks);
        tasks = tasks[..exam.Selection];

        var index = 0;
        foreach (var taskId in tasks)
        {
            var task = new ExamTaskControl(taskId, index);
            task.Pressed += OnTaskPressed;
            TaskContainers.AddChild(task);
            index++;
        }

        // Refresh window lol
        SetSize = Size;
    }

    private void OnSendPress(BaseButton.ButtonEventArgs _)
    {
        Send();
        Close();
    }

    private void OnTaskPressed(ProtoId<ExamTaskPrototype> task, int answer)
    {
        if (!_answers.TryAdd(task, answer))
            _answers.Remove(task);

        var disabled = _answers.Count != _answerCount;

        SendButton.Label.FontColorOverride = disabled ? Color.FromHex("#454545") : Color.White;
        SendButton.Disabled = disabled;
        SendButton.Text =
            Loc.GetString(disabled ? "ui-imperial-exam-send-button-disabled" : "ui-imperial-exam-send-button");
    }

    private void Send()
    {
        if (_exam is null || _sent)
            return;

        _sent = true;
        Sent?.Invoke(_exam.Value, _answers);
    }
}
