using Content.Shared.Imperial.Medieval.Skills;
using Robust.Client.AutoGenerated;
using Robust.Client.UserInterface.CustomControls;
using Robust.Client.UserInterface.XAML;
using Robust.Shared.Prototypes;

namespace Content.Client.Imperial.Medieval.Skills.UI;

[GenerateTypedNameReferences]
public sealed partial class AdminSkillsMenu : DefaultWindow
{
    public Action<string, int>? LevelSet;
    public NetEntity Target;

    public AdminSkillsMenu(NetEntity target, Dictionary<string, int> skills)
    {
        RobustXamlLoader.Load(this);
        Main.RemoveAllChildren();
        var proto = IoCManager.Resolve<IPrototypeManager>();

        Target = target;

        foreach (var (skill, level) in skills)
        {
            var skillEntry = new MiniSkillEntry(Loc.GetString(proto.Index<SkillPrototype>(skill).Name), level)
            {
                HorizontalExpand = true,
            };
            skillEntry.LevelSet += newLevel =>
            {
                skillEntry.Level = newLevel;
                LevelSet?.Invoke(skill, newLevel);
            };

            Main.AddChild(skillEntry);
        }
    }
}
