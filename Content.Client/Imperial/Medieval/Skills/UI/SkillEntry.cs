using Content.Client.Imperial.Roadmap.UI;
using Content.Shared.Imperial.Medieval.Language;
using Content.Shared.Imperial.Medieval.Skills;
using Robust.Client.AutoGenerated;
using Robust.Client.Graphics;
using Robust.Client.UserInterface;
using Robust.Client.UserInterface.Controls;
using Robust.Client.UserInterface.XAML;
using Robust.Client.Utility;
using Robust.Shared.Utility;

namespace Content.Client.Imperial.Medieval.Skills.UI;

[GenerateTypedNameReferences]
public sealed partial class SkillEntry : Control
{
    public string? Label
    {
        get => Name.Text;
        set => Name.Text = value;
    }

    public int Level
    {
        get => _level;
        set
        {
            _level = value;
            UpdateVisualizer(value);
        }
    }

    private int _level = 1;

    private Color _color;

    public Action<int>? LevelSet;
    public Action? Decreased;

    public SkillEntry(string name, int level, SpriteSpecifier icon, Color color)
    {
        RobustXamlLoader.Load(this);

        Name.Text = name;
        _color = color;
        Level = level;
        Icon.Texture = icon.Frame0();
        IncreaseButton.OnPressed += _ => LevelSet?.Invoke(Level + 1);
        DecreaseButton.OnPressed += _ => LevelSet?.Invoke(Level - 1);
    }

    public void UpdateVisualizer(int level)
    {
        VisualizerContainer.RemoveAllChildren();
        _level = level;

        for (var i = 0; i < 20; i++)
        {
            var diff = 20 - level;

            var panel = new BorderedPanelContainer()
            {
                PanelOverride = new StyleBoxFlat(Color.White),
                BackgroundPanelColor = i >= diff ? _color : Color.Gray,
                BorderRadius = 35f,
                HorizontalExpand = true,
                VerticalExpand = true,
                Margin = new(2, 4),
            };
            VisualizerContainer.AddChild(panel);
        }
    }
}
