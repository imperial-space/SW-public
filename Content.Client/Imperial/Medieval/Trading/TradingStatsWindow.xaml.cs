using System.Linq;
using Robust.Client.AutoGenerated;
using Robust.Client.UserInterface;
using Robust.Client.UserInterface.CustomControls;
using Robust.Client.UserInterface.XAML;

namespace Content.Client.Imperial.Medieval.Trading;

[GenerateTypedNameReferences]
public sealed partial class TradingStatsWindow : DefaultWindow
{
    public void PopulateEntries(Dictionary<string, float> entries)
    {
        Entries.DisposeAllChildren();
        var sortedEntries = entries.OrderByDescending(d => d.Value);
        foreach (var (name, reputation) in sortedEntries)
        {
            var roundedReputation = MathF.Round(reputation, 1);
            var entryControl = new TradingStatsEntry(name, roundedReputation);
            Entries.AddChild(entryControl);
        }
    }

    public TradingStatsWindow()
    {
        RobustXamlLoader.Load(this);
    }

}
