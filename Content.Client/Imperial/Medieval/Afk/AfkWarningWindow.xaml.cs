using Content.Shared.Imperial.Medieval.CCVar;
using Robust.Client.AutoGenerated;
using Robust.Client.UserInterface.Controls;
using Robust.Client.UserInterface.CustomControls;
using Robust.Client.UserInterface.XAML;
using Robust.Shared.Configuration;
using Robust.Shared.Timing;

namespace Content.Client.Imperial.Medieval.Afk;

[GenerateTypedNameReferences]
public sealed partial class AfkWarningWindow : DefaultWindow
{

    public float Remaining;
    private float _tickAccumulator;

    public string AfkText = "";
    public string KickText = "";

    public AfkWarningWindow()
    {
        RobustXamlLoader.Load(this);
        IoCManager.InjectDependencies(this);
    }

    protected override void FrameUpdate(FrameEventArgs args)
    {
        base.FrameUpdate(args);

        _tickAccumulator += args.DeltaSeconds;
        if (!(_tickAccumulator >= 1f))
            return;

        _tickAccumulator -= 1f;
        Remaining--;

        if (Remaining < 0)
            Remaining = 0;

        var kickText = $"{Math.Floor(Remaining)} сек";

        UpdateLabel(AfkText, kickText);
    }

    private void UpdateLabel(string afk, string kick)
    {
        WarningText.Text = Loc.GetString("afk-warning-text", ("afk", afk), ("kick", kick));
    }

    public Label WarningText => Warning;
}
