using Content.Client.Administration.UI.CustomControls;
using Content.Client.Message;
using Content.Client.UserInterface.Systems.Guidebook;
using Content.Shared.Guidebook;
using Content.Shared.Imperial.Medieval.Lycantropy;
using Robust.Client.AutoGenerated;
using Robust.Client.UserInterface;
using Robust.Client.UserInterface.Controls;
using Robust.Client.UserInterface.CustomControls;
using Robust.Client.UserInterface.XAML;
using Robust.Shared.Input;
using Robust.Shared.Prototypes;
using System.Linq;
using System.Numerics;

namespace Content.Client.Imperial.Medieval.Lycantropy.UI;

[GenerateTypedNameReferences]
public sealed partial class LycantropyProgressMenu : DefaultWindow
{
    [Dependency] private readonly IPrototypeManager _proto = default!;

    public Action<ProtoId<LycantropyAbilityPrototype>>? BuyPressed;

    public int Points = 0;

    private bool _draggin = false;
    private Vector2 _position = Vector2.Zero;

    public LycantropyProgressMenu()
    {
        IoCManager.InjectDependencies(this);
        RobustXamlLoader.Load(this);

        Panel.OnKeyBindDown += KeybindDown;
        Panel.OnKeyBindUp += KeybindUp;
        Recenter.OnPressed += args =>
        {
            _position = Vector2.Zero;
            foreach (var item in Abilities.Children)
            {
                if (item is not LycantropyAbilityEntry entry)
                    continue;

                LayoutContainer.SetPosition(entry, entry.Proto.Position * 6 + _position);
            }
        };

        Info.OnPressed += args =>
        {
            var guidebookController = UserInterfaceManager.GetUIController<GuidebookUIController>();

            if (_proto.TryIndex<GuideEntryPrototype>("Antagonists", out var guideRoot))
            {
                var dict = new Dictionary<ProtoId<GuideEntryPrototype>, GuideEntry>() { { "Antagonists", guideRoot } };
                guidebookController.OpenGuidebook(dict, includeChildren: true, selected: "Werewolf");
            }

        };
    }

    public void Populate(List<ProtoId<LycantropyAbilityPrototype>> unlocked, int points)
    {
        Points = points;
        PointsCounter.SetMarkup($"Очки ярости: {Points}");

        var prototypes = _proto.EnumeratePrototypes<LycantropyAbilityPrototype>();

        if (Abilities.ChildCount > 0)
        {
            foreach (var item in Abilities.Children)
            {
                if (item is not LycantropyAbilityEntry entry)
                    continue;

                entry.UpdateAllowance(unlocked.Contains(entry.Proto.ID), ReqsMet(unlocked, entry.Proto) ? points : -1);
            }
        }
        else
        {
            foreach (var item in prototypes)
            {
                var entry = new LycantropyAbilityEntry(item);
                entry.UpdateAllowance(unlocked.Contains(item.ID), ReqsMet(unlocked, item) ? points : -1);
                entry.BuyPressed += args => BuyPressed?.Invoke(args);

                Abilities.AddChild(entry);
                LayoutContainer.SetPosition(entry, item.Position * 6 + _position);
            }
        }
    }

    private bool ReqsMet(List<ProtoId<LycantropyAbilityPrototype>> unlocked, LycantropyAbilityPrototype proto)
    {
        foreach (var item in proto.Required)
        {
            if (!unlocked.Contains(item))
                return false;
        }

        return true;
    }

    #region Drag handle
    protected override void MouseMove(GUIMouseMoveEventArgs args)
    {
        base.MouseMove(args);

        if (_draggin)
        {
            _position += args.Relative;

            foreach (var child in Abilities.Children)
            {
                LayoutContainer.SetPosition(child, child.Position + args.Relative);
            }
        }
    }
    private void KeybindDown(GUIBoundKeyEventArgs args)
    {
        if (args.Function == EngineKeyFunctions.Use)
            _draggin = true;
    }

    private void KeybindUp(GUIBoundKeyEventArgs args)
    {
        if (args.Function == EngineKeyFunctions.Use)
            _draggin = false;
    }


    protected override DragMode GetDragModeFor(Vector2 relativeMousePos)
    {
        return _draggin ? DragMode.None : base.GetDragModeFor(relativeMousePos);
    }
    #endregion

}
