using Content.Client.Message;
using Content.Shared.Imperial.Medieval.Lycantropy;
using Robust.Client.AutoGenerated;
using Robust.Client.Graphics;
using Robust.Client.UserInterface;
using Robust.Client.UserInterface.XAML;
using Robust.Client.Utility;
using Robust.Shared.Prototypes;

namespace Content.Client.Imperial.Medieval.Lycantropy.UI;

[GenerateTypedNameReferences]
public sealed partial class LycantropyAbilityEntry : Control
{
    private bool _collapsed = true;
    public Action<ProtoId<LycantropyAbilityPrototype>>? BuyPressed;
    public LycantropyAbilityPrototype Proto;

    public LycantropyAbilityEntry(LycantropyAbilityPrototype item)
    {
        IoCManager.InjectDependencies(this);
        RobustXamlLoader.Load(this);

        BuyButton.Color = Color.FromHex("#000000FF");
        BuyButton.BorderColor = Color.FromHex("#e8e8e8FF");
        BuyButton.HoveredColor = Color.FromHex("#424242FF");

        BuyButton.UpdateColor();

        AbilityName.SetMessage(Loc.GetString(item.Name));
        AbilityIcon.Texture = item.Icon.Frame0();

        AbilityDesc.SetMessage(Loc.GetString(item.Desc));
        Cost.SetMarkup(Loc.GetString("imperial-hm-ui-costrage", ("amount", $"{item.Cost}")));

        BuyButton.OnPressed += args => BuyPressed?.Invoke(item.ID);

        Proto = item;
    }

    public void UpdateAllowance(bool unlocked, int allowedPoints)
    {
        BuyButton.Button.Disabled = allowedPoints < Proto.Cost || unlocked;

        Color color = Color.LimeGreen;
        if (!unlocked)
            color = allowedPoints < Proto.Cost ? Color.Crimson : Color.Gold;

        Panel.PanelOverride = new StyleBoxFlat()
        {
            BackgroundColor = Color.Black,
            BorderColor = color,
            BorderThickness = new(1),
        };
    }
}
