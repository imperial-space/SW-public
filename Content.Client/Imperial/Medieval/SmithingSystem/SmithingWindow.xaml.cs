using System.Numerics;
using Content.Client.UserInterface.Controls;
using Content.Shared.Imperial.Medieval.SmithingSystem.Bui;
using Robust.Client.AutoGenerated;
using Robust.Client.GameObjects;
using Robust.Client.UserInterface;
using Robust.Client.UserInterface.Controls;
using Robust.Client.UserInterface.XAML;
using Robust.Shared.Input;
using Robust.Shared.Random;
using Robust.Shared.Timing;
using Robust.Shared.Utility;

namespace Content.Client.Imperial.Medieval.SmithingSystem;

[GenerateTypedNameReferences]
public sealed partial class SmithingWindow : FancyWindow
{
    private readonly List<(Control Ctrl, Box2 Rect)> _activeTargets = new();

    [Dependency] private readonly IEntityManager _entityManager = default!;

    private readonly Control _missCatcher = new();

    private readonly IRobustRandom _random;
    private readonly SpriteSystem _sprite;

    private readonly Vector2 _targetSize = new(64, 64);
    private int _completedSteps;

    private SmithGameData? _gameData;

    private bool _gameStarted;

    private Stack<SmithStepData>? _pendingSteps;
    private int _points;

    private float _ticker;
    private int _totalSteps;

    public SmithingWindow()
    {
        RobustXamlLoader.Load(this);
        IoCManager.InjectDependencies(this);

        _sprite = _entityManager.System<SpriteSystem>();
        _random = IoCManager.Resolve<IRobustRandom>();

        LayoutContainer.SetAnchorPreset(ItemSpriteView, LayoutContainer.LayoutPreset.Wide);

        MainLayout.MouseFilter = MouseFilterMode.Pass;
        _missCatcher.MouseFilter = MouseFilterMode.Stop;
        MainLayout.AddChild(_missCatcher);
        LayoutContainer.SetAnchorPreset(_missCatcher, LayoutContainer.LayoutPreset.Wide);
        LayoutContainer.SetPosition(_missCatcher, Vector2.Zero);

        _missCatcher.OnKeyBindDown += args =>
        {
            if (args.Function != EngineKeyFunctions.UIClick || !_gameStarted)
                return;

            HitMissed?.Invoke();
            _points--;
        };


        StartGameButton.OnPressed += _ =>
        {
            StartGamePressed?.Invoke();
            StartGameButton.Visible = false;
        };
    }

    public event Action<SmithHitMesage>? TargetHit;
    public event Action? HitMissed;
    public event Action<SmithHitMesage>? TargetExpired;
    public event Action? StartGamePressed;
    public event Action<int>? GameEnded;

    public void StartGame(SmithGameData gameData)
    {
        _gameData = gameData;
        _pendingSteps = CloneSteps(gameData.Steps);

        _gameStarted = true;
        _ticker = 0f;
        _points = 0;
        _completedSteps = 0;
        _totalSteps = _pendingSteps.Count;

        _activeTargets.Clear();
        RemoveAllTargets();

        ItemSpriteView.Texture = _sprite.GetPrototypeIcon(gameData.ItemProtoId).Default;
    }

    protected override void FrameUpdate(FrameEventArgs args)
    {
        base.FrameUpdate(args);

        if (!_gameStarted || _gameData == null || _pendingSteps == null)
            return;

        StepsLabel.Text = $"{_completedSteps} / {_totalSteps}";
        PointsLabel.Text = $"Очки: {_points}";

        if (_completedSteps >= _totalSteps)
        {
            _gameStarted = false;
            GameEnded?.Invoke(_points);
            return;
        }

        _ticker += args.DeltaSeconds;

        if (_ticker < _gameData.SpawnTime)
            return;

        if (!_pendingSteps.TryPop(out var targetData))
            return;

        CreateTarget(targetData.GoodHitTime, targetData.PerfectHitTime, targetData.IsPenaltyActivator);
        _ticker = 0f;
    }

    private void CreateTarget(float grayTime, float goldTime, bool isPenaltyActivator)
    {
        var size = MainLayout.Size;

        var maxX = size.X - _targetSize.X;
        var maxY = size.Y - _targetSize.Y;
        if (maxX <= 1 || maxY <= 1)
            return;

        const float padding = 8f;
        const int maxTries = 30;

        Vector2 chosenPos = default;
        Box2 chosenRect = default;
        var found = false;

        for (var i = 0; i < maxTries; i++)
        {
            var x = _random.NextFloat(0f, maxX);
            var y = _random.NextFloat(0f, maxY);

            var rect = new Box2(
                new Vector2(x - padding, y - padding),
                new Vector2(x + _targetSize.X + padding, y + _targetSize.Y + padding));

            if (IntersectsAny(rect))
                continue;

            chosenPos = new Vector2(x, y);
            chosenRect = rect;
            found = true;
            break;
        }

        if (!found)
            return;

        var button = new SmithTargetButton(grayTime, goldTime, isPenaltyActivator)
        {
            SetSize = _targetSize,
        };

        MainLayout.AddChild(button);
        LayoutContainer.SetPosition(button, chosenPos);

        _activeTargets.Add((button, chosenRect));

        void Cleanup()
        {
            _activeTargets.RemoveAll(t => t.Ctrl == button);
            if (button.Parent == MainLayout)
                MainLayout.RemoveChild(button);
        }

        button.Clicked += state =>
        {
            button.DisableInput();

            var isPenalty = isPenaltyActivator;

            if (state == SmithHitState.Good)
                _points++;
            else if (state == SmithHitState.Penalty)
                _points -= 6;

            if (!isPenalty)
            {
                _completedSteps++;
                TargetHit?.Invoke(new SmithHitMesage(state, true));
            }
            else
            {
                TargetHit?.Invoke(new SmithHitMesage(state, false));

                _pendingSteps?.Push(new SmithStepData
                {
                    State = SmithHitState.Missed,
                    PerfectHitTime = goldTime,
                    GoodHitTime = grayTime,
                    IsPenaltyActivator = false,
                });
            }

            Cleanup();
        };

        button.Expired += expiredPenalty =>
        {
            button.DisableInput();

            var isPenalty = expiredPenalty;
            var state = isPenalty ? SmithHitState.Neutral : SmithHitState.Missed;

            if (!isPenalty)
            {
                _points--;
                _completedSteps++;
                TargetExpired?.Invoke(new SmithHitMesage(state, true));
            }
            else
            {
                TargetExpired?.Invoke(new SmithHitMesage(state, false));

                _pendingSteps?.Push(new SmithStepData
                {
                    State = SmithHitState.Missed,
                    PerfectHitTime = goldTime,
                    GoodHitTime = grayTime,
                    IsPenaltyActivator = false,
                });
            }

            Cleanup();
        };
    }

    private bool IntersectsAny(Box2 rect)
    {
        foreach (var t in _activeTargets)
        {
            if (t.Rect.Intersects(rect))
                return true;
        }

        return false;
    }

    private void RemoveAllTargets()
    {
        for (var i = MainLayout.ChildCount - 1; i >= 0; i--)
        {
            if (MainLayout.GetChild(i) is SmithTargetButton b)
                MainLayout.RemoveChild(b);
        }
    }

    private static Stack<SmithStepData> CloneSteps(Stack<SmithStepData> src)
    {
        var arr = src.ToArray();
        Array.Reverse(arr);
        return new Stack<SmithStepData>(arr);
    }

    private sealed class SmithTargetButton : TextureButton
    {
        private static readonly ResPath[] _spriteStates =
        {
            new("/Textures/Imperial/Medieval/Interface/sparkle.rsi/s_0.png"),
            new("/Textures/Imperial/Medieval/Interface/sparkle.rsi/s_1.png"),
            new("/Textures/Imperial/Medieval/Interface/sparkle.rsi/s_2.png"),
            new("/Textures/Imperial/Medieval/Interface/sparkle.rsi/s_3.png"),
        };

        private readonly float _goldTime;

        private readonly float _grayTime;

        private readonly bool _isPenaltyActivator;

        private int _currentStage;

        private float _deltaTime;

        private bool _expiredOrHandled;

        public SmithTargetButton(float grayTime, float goldTime, bool isPenaltyActivator)
        {
            MouseFilter = MouseFilterMode.Stop;
            _goldTime = goldTime;
            _grayTime = grayTime;
            _isPenaltyActivator = isPenaltyActivator;

            TexturePath = _spriteStates[0].ToString();

            OnPressed += _ =>
            {
                if (_expiredOrHandled)
                    return;

                _expiredOrHandled = true;

                var state = _currentStage == 1
                    ? (_isPenaltyActivator ? SmithHitState.Penalty : SmithHitState.Good)
                    : SmithHitState.Neutral;
                Clicked?.Invoke(state);
            };
        }

        public event Action<SmithHitState>? Clicked;
        public event Action<bool>? Expired;

        public void DisableInput()
        {
            _expiredOrHandled = true;
            Disabled = true;
            MouseFilter = MouseFilterMode.Ignore;
        }

        protected override void FrameUpdate(FrameEventArgs args)
        {
            base.FrameUpdate(args);

            if (_expiredOrHandled)
                return;

            _deltaTime += args.DeltaSeconds;

            switch (_currentStage)
            {
                case 0:
                    if (_deltaTime >= _grayTime)
                    {
                        _deltaTime = 0;
                        _currentStage = 1;
                        TexturePath = (_isPenaltyActivator ? _spriteStates[3] : _spriteStates[1]).ToString();
                    }

                    break;

                case 1:
                    if (_deltaTime >= _goldTime)
                    {
                        _deltaTime = 0;
                        _currentStage = 2;
                        TexturePath = _spriteStates[2].ToString();
                    }

                    break;

                case 2:
                    if (_deltaTime >= _grayTime)
                    {
                        _expiredOrHandled = true;
                        Expired?.Invoke(_isPenaltyActivator);
                    }

                    break;
            }
        }
    }
}
