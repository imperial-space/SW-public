using Content.Shared.Imperial.Medieval.Waystones;
using Robust.Client.AutoGenerated;
using Robust.Client.UserInterface.CustomControls;
using Robust.Client.UserInterface.Controls;
using Robust.Client.UserInterface.XAML;

namespace Content.Client.Imperial.Medieval.Waystones.UI;

[GenerateTypedNameReferences]
public sealed partial class WaystoneAdminMenu : DefaultWindow
{
    public event Action<int, int>? OnApplyPressed;

    public WaystoneAdminMenu()
    {
        RobustXamlLoader.Load(this);

        ApplyButton.OnPressed += _ =>
        {
            if (int.TryParse(PriceInInput.Text, out var priceIn) &&
                int.TryParse(PriceOutInput.Text, out var priceOut))
            {
                OnApplyPressed?.Invoke(priceIn, priceOut);
                Close();
            }
        };
    }

    public void UpdateValues(int currentPriceIn, int currentPriceOut)
    {
        PriceInInput.Text = currentPriceIn.ToString();
        PriceOutInput.Text = currentPriceOut.ToString();
    }
}
