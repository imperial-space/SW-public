using Content.Client.Imperial.Medieval.PlayerCreations.Paintings;
using Content.Shared.Imperial.Medieval.PlayerCreations;
using Robust.Client.AutoGenerated;
using Robust.Client.Graphics;
using Robust.Client.Player;
using Robust.Client.UserInterface;
using Robust.Client.UserInterface.Controls;
using Robust.Client.UserInterface.CustomControls;
using Robust.Client.UserInterface.XAML;

namespace Content.Client.Imperial.Medieval.PlayerCreations.Administration;

[GenerateTypedNameReferences]
public sealed partial class BookEntry : Control
{
    [Dependency] private readonly IPlayerManager _playerManager = default!;

    public BookEntry(CreationBook book, Action? onAccept = null, Action? onDecline = null, bool buttonsEnabled = true)
    {
        RobustXamlLoader.Load(this);
        IoCManager.InjectDependencies(this);

        BookName.SetMessage(book.Name);
        BookAuthor.SetMessage(book.Author);

        ReadButton.OnPressed += _ =>
        {
            var window = new BookPreviewWindow(book.Text);
            window.Open();
        };

        MoreButton.OnPressed += _ => OpenMoreWindow(book);

        if (!buttonsEnabled)
        {
            AcceptButton.Visible = false;
            DeclineButton.Visible = false;
        }
        else
        {
            AcceptButton.OnPressed += _ => onAccept?.Invoke();
            DeclineButton.OnPressed += _ => onDecline?.Invoke();
        }
    }

    private void OpenMoreWindow(CreationBook book)
    {
        var moreWindow = new MoreWindow();

        var moreDict = new Dictionary<string, string>();
        moreDict.Add(Loc.GetString("creations-info-name"), book.Name);
        moreDict.Add(Loc.GetString("creations-info-description"), book.Description);
        moreDict.Add(Loc.GetString("creations-info-author"), book.Author);
        var data = _playerManager.GetPlayerData(book.SenderUserId);
        moreDict.Add(Loc.GetString("creations-info-author-nick"), data.UserName);
        moreDict.Add(Loc.GetString("creations-info-creation-time"), book.CreationTime.ToString("dd.MM.yyyy HH:mm"));

        moreWindow.Populate(moreDict);
        moreWindow.Open();
    }
}
