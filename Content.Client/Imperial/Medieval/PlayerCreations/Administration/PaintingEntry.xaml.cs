using Content.Client.Imperial.Medieval.PlayerCreations.Paintings;
using Content.Shared.Imperial.Medieval.PlayerCreations;
using Robust.Client.AutoGenerated;
using Robust.Client.Graphics;
using Robust.Client.UserInterface;
using Robust.Client.UserInterface.Controls;
using Robust.Client.UserInterface.CustomControls;
using Robust.Client.UserInterface.XAML;

namespace Content.Client.Imperial.Medieval.PlayerCreations.Administration;

[GenerateTypedNameReferences]
public sealed partial class PaintingEntry : Control
{
    public PaintingEntry(IClyde clyde, CreationPaintingMessage message, Action? onAccept = null, Action? onDecline = null)
    {
        RobustXamlLoader.Load(this);

        // PaintingView.SetEntity(message.Painting);

        var texture = CanvasSystem.GetTextureFromColorArray(clyde, message.Painting);

        PaintingView.Texture = texture;

        PaintingName.SetMessage(message.Name);
        PaintingAuthor.SetMessage(message.Author);

        AcceptButton.OnPressed += _ => onAccept?.Invoke();
        DeclineButton.OnPressed += _ => onDecline?.Invoke();
    }
}
