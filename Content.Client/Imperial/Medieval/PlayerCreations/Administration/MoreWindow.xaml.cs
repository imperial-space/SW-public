using Content.Shared.Imperial.Medieval.PlayerCreations;
using Robust.Client.AutoGenerated;
using Robust.Client.UserInterface.Controls;
using Robust.Client.UserInterface.CustomControls;
using Robust.Client.UserInterface.XAML;

namespace Content.Client.Imperial.Medieval.PlayerCreations.Administration;

[GenerateTypedNameReferences]
public sealed partial class MoreWindow : DefaultWindow
{
    public MoreWindow()
    {
        IoCManager.InjectDependencies(this);
        RobustXamlLoader.Load(this);
    }

    private (RichTextLabel, LineEdit) CreateLine(string key, string value, Button? button, bool editable = true)
    {
        var keyLabel = new RichTextLabel
        {
            HorizontalAlignment = HAlignment.Stretch,
            SetWidth = 150,
        };
        var valueLabel = new LineEdit()
        {
            HorizontalAlignment = HAlignment.Stretch,
            SetWidth = 150,
        };

        keyLabel.Margin = new Thickness(0, 0, 5, 20);
        valueLabel.Margin = new Thickness(0, 0, 0, 20);

        Contents.AddChild(keyLabel);
        Contents.AddChild(valueLabel);

        keyLabel.SetMessage(key);
        valueLabel.Text = value;

        valueLabel.Editable = editable;

        if(button != null)
            valueLabel.OnTextChanged += _ => button.Disabled = false;

        return (keyLabel, valueLabel);
    }

    public void PopulateCreation(CreationInfo info, bool editable, Action<EditedCreationData>? onEdit)
    {
        var utc = DateTime.SpecifyKind(info.CreationTime, DateTimeKind.Utc);
        var offset = new DateTimeOffset(utc).ToOffset(TimeSpan.FromHours(3));

        var saveButton = new Button
        {
            Text = "Сохранить",
            HorizontalAlignment = HAlignment.Stretch,
            ToolTip = "Изменения вступят в силу в следующем раунде",
            Disabled = true,
        };

        var (_, nameValue) = CreateLine(Loc.GetString("creations-info-name"), info.Name, saveButton, editable);
        var (_, descValue) = CreateLine(Loc.GetString("creations-info-description"), info.Description, saveButton, editable);
        var (_, authorValue) = CreateLine(Loc.GetString("creations-info-author"), info.Author, saveButton, editable);
        CreateLine(Loc.GetString("creations-info-author-nick"), info.SenderUserName, saveButton, false);
        CreateLine(Loc.GetString("creations-info-creation-time"), offset.ToString("dd.MM.yyyy HH:mm"), saveButton, false);

        saveButton.OnPressed += _ =>
        {
            var edited = new EditedCreationData
            {
                Name = nameValue.Text,
                Author = authorValue.Text,
                Description = descValue.Text,
            };

            saveButton.Disabled = true;
            onEdit?.Invoke(edited);
        };

        if (editable)
        {
            Contents.AddChild(saveButton);
        }
    }

    public void PopulatePainting(CreationPaintingMessage message, bool editable, Action<EditedCreationData>? onEdit)
    {
        PopulateCreation(new CreationInfo
        {
            Name = message.Name,
            Description = message.Description,
            Author = message.Author,
            SenderUserName = message.SenderUserName,
            CreationTime = message.CreationTime,
        }, editable, onEdit);
    }

    public void PopulateBook(CreationBook message, bool editable, Action<EditedCreationData>? onEdit)
    {
        PopulateCreation(new CreationInfo
        {
            Name = message.Name,
            Description = message.Description,
            Author = message.Author,
            SenderUserName = message.SenderUserName,
            CreationTime = message.CreationTime,
        }, editable, onEdit);
    }

    public void Populate(Dictionary<string, string> content)
    {
        foreach (var (key, value) in content)
        {
            var keyLabel = new RichTextLabel
            {
                HorizontalAlignment = HAlignment.Stretch,
                SetWidth = 150,
            };
            var valueLabel = new LineEdit()
            {
                HorizontalAlignment = HAlignment.Stretch,
                SetWidth = 150,
            };

            keyLabel.Margin = new Thickness(0, 0, 5, 20);
            valueLabel.Margin = new Thickness(0, 0, 0, 20);

            Contents.AddChild(keyLabel);
            Contents.AddChild(valueLabel);

            keyLabel.SetMessage(key);
            valueLabel.Text = value;
        }
    }

}
