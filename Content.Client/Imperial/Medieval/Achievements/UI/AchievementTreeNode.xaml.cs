using System;
using System.Numerics;
using Content.Shared.Imperial.Medieval.Achievements;
using Robust.Client.AutoGenerated;
using Robust.Client.GameObjects;
using Robust.Client.Graphics;
using Robust.Client.UserInterface;
using Robust.Client.UserInterface.Controls;
using Robust.Client.UserInterface.XAML;
using Robust.Client.ResourceManagement;
using Robust.Shared.Input;
using Robust.Shared.Utility;

namespace Content.Client.Imperial.Medieval.Achievements.UI;

[GenerateTypedNameReferences]
public sealed partial class AchievementTreeNode : PanelContainer
{
    public event Action<AchievementPrototype>? OnSelected;

    public AchievementPrototype Proto { get; }
    public bool Unlocked       => _state == NodeState.Unlocked;
    public bool IsQuestionMark => _state == NodeState.Question;

    private NodeState _state;
    private AchievementRarityPrototype? _rarity;

    private static readonly ResPath QuestionIconPath =
        new("/Textures/Imperial/Medieval/Interface/Achievements/question.png");

    private enum NodeState : byte
    {
        Question,
        Unmet,
        Available,
        Unlocked,
    }

    private readonly struct Palette
    {
        public readonly Color Border;
        public readonly Color Background;
        public readonly Color? IconModulate;

        public Palette(Color border, Color background, Color? iconModulate)
        {
            Border       = border;
            Background   = background;
            IconModulate = iconModulate;
        }
    }

    private static readonly Color UnlockedBorder  = Color.FromHex("#5c8a3e");
    private static readonly Color AvailableBorder = Color.FromHex("#8b6914");
    private static readonly Color UnmetBorder     = Color.FromHex("#251b12");
    private static readonly Color QuestionBorder  = Color.FromHex("#4a3a28");

    private static readonly Color UnlockedBg  = Color.FromHex("#1c1408");
    private static readonly Color AvailableBg = Color.FromHex("#160f08");
    private static readonly Color UnmetBg     = Color.FromHex("#0d0905");
    private static readonly Color QuestionBg  = Color.FromHex("#100c07");

    private static readonly Color LockedIconTint   = Color.FromHex("#55443388");
    private static readonly Color QuestionIconTint = Color.FromHex("#7a6040cc");

    public AchievementTreeNode(
        AchievementPrototype proto,
        bool unlocked,
        bool prereqsMet,
        bool questionMark,
        SpriteSystem spriteSystem,
        AchievementRarityPrototype? rarity = null)
    {
        RobustXamlLoader.Load(this);

        Proto   = proto;
        _state  = ResolveState(unlocked, prereqsMet, questionMark);
        _rarity = rarity;

        var inert = proto.Visibility == AchievementVisibility.Hidden && !unlocked && !questionMark;
        MouseFilter = inert ? MouseFilterMode.Ignore : MouseFilterMode.Stop;

        TooltipSupplier = _ => new AchievementTooltipPopup(Proto, Unlocked, spriteSystem, IsQuestionMark);
        ApplyVisuals(spriteSystem);
    }

    public void Refresh(
        bool unlocked,
        bool prereqsMet,
        bool questionMark,
        SpriteSystem spriteSystem,
        AchievementRarityPrototype? rarity)
    {
        _state  = ResolveState(unlocked, prereqsMet, questionMark);
        _rarity = rarity;

        ApplyVisuals(spriteSystem);
    }

    private static NodeState ResolveState(bool unlocked, bool prereqsMet, bool questionMark)
    {
        if (questionMark)
            return NodeState.Question;

        if (unlocked)
            return NodeState.Unlocked;

        if (!prereqsMet)
            return NodeState.Unmet;

        return NodeState.Available;
    }

    private void ApplyVisuals(SpriteSystem spriteSystem)
    {
        ApplyIcon(spriteSystem);

        var palette = GetPalette();

        BorderPanel.PanelOverride = new StyleBoxFlat
        {
            BackgroundColor             = palette.Background,
            BorderColor                 = palette.Border,
            BorderThickness             = new Thickness(2),
            ContentMarginLeftOverride   = 4,
            ContentMarginRightOverride  = 4,
            ContentMarginTopOverride    = 4,
            ContentMarginBottomOverride = 4,
        };
    }

    private void ApplyIcon(SpriteSystem spriteSystem)
    {
        switch (_state)
        {
            case NodeState.Question:
                var cache = IoCManager.Resolve<IResourceCache>();
                if (cache.TryGetResource<TextureResource>(QuestionIconPath, out var tex))
                    AchievementIcon.Texture = tex.Texture;
                AchievementIcon.Visible              = true;
                AchievementIcon.ModulateSelfOverride = QuestionIconTint;
                break;

            case NodeState.Unlocked:
                AchievementIcon.Texture              = spriteSystem.Frame0(Proto.Icon);
                AchievementIcon.Visible              = true;
                AchievementIcon.ModulateSelfOverride = null;
                break;

            case NodeState.Available:
            case NodeState.Unmet:
                if (Proto.Visibility == AchievementVisibility.Hidden)
                {
                    AchievementIcon.Visible = false;
                }
                else
                {
                    AchievementIcon.Texture              = spriteSystem.Frame0(Proto.Icon);
                    AchievementIcon.Visible              = true;
                    AchievementIcon.ModulateSelfOverride = LockedIconTint;
                }
                break;
        }
    }

    private Palette GetPalette()
    {
        return _state switch
        {
            NodeState.Question  => new Palette(QuestionBorder, QuestionBg, QuestionIconTint),
            NodeState.Unlocked  => new Palette(_rarity != null ? Color.FromHex(_rarity.AccentColor) : UnlockedBorder,
                                               UnlockedBg, null),
            NodeState.Available => new Palette(AvailableBorder, AvailableBg, LockedIconTint),
            NodeState.Unmet     => new Palette(UnmetBorder,     UnmetBg,     LockedIconTint),
            _                   => new Palette(UnmetBorder,     UnmetBg,     null),
        };
    }

    public void ApplyZoom(float zoom)
    {
    }

    private bool _attemptingSelect;

    protected override void KeyBindDown(GUIBoundKeyEventArgs args)
    {
        base.KeyBindDown(args);

        if (args.Function == EngineKeyFunctions.Use)
            _attemptingSelect = true;
    }

    protected override void KeyBindUp(GUIBoundKeyEventArgs args)
    {
        base.KeyBindUp(args);

        if (args.Function != EngineKeyFunctions.Use)
            return;

        if (_attemptingSelect && !IsQuestionMark &&
            HasPoint((args.PointerLocation.Position - GlobalPixelPosition) / UIScale))
        {
            OnSelected?.Invoke(Proto);
        }

        _attemptingSelect = false;
    }
}
