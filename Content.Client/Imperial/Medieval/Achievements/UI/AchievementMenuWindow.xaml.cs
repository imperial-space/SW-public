using System;
using System.Linq;
using System.Collections.Generic;
using Content.Shared.Imperial.Medieval.Achievements;
using Content.Client.Imperial.Medieval.CapturePoint.UI.MedievalWindow;
using Robust.Client.AutoGenerated;
using Robust.Client.GameObjects;
using Robust.Client.ResourceManagement;
using Robust.Client.UserInterface.Controls;
using Robust.Client.UserInterface.XAML;
using Robust.Shared.Prototypes;
using Robust.Client.Graphics;
using Robust.Shared.Utility;

namespace Content.Client.Imperial.Medieval.Achievements.UI;

public enum AchievementSortMode
{
    Alphabetical,
    Unlocked,
    Difficulty
}

[GenerateTypedNameReferences]
public sealed partial class AchievementMenuWindow : MedievalWindow
{
    private readonly IPrototypeManager _proto;
    private readonly IResourceCache _cache;
    private readonly SpriteSystem _spriteSystem;

    private IReadOnlySet<string> _unlocked = new HashSet<string>();
    private IReadOnlyDictionary<string, float> _percents = new Dictionary<string, float>();
    private IReadOnlyDictionary<string, Dictionary<string, int>> _progress = new Dictionary<string, Dictionary<string, int>>();

    private float _progressPct  = 0f;
    private AchievementSortMode _currentSort  = AchievementSortMode.Alphabetical;
    private AchievementPrototype? _selectedProto;

    public AchievementMenuWindow(IPrototypeManager proto, IResourceCache cache, SpriteSystem spriteSystem)
    {
        RobustXamlLoader.Load(this);
        _proto = proto;
        _cache = cache;
        _spriteSystem = spriteSystem;

        SearchBox.OnTextChanged += _ => RebuildList();
        ProgressBarBg.OnResized += UpdateProgressBar;
        SortCycleButton.OnPressed += _ => CycleSort();

        UpdateSortButtonText();
    }

    public void Populate(
        IReadOnlySet<string> unlocked,
        IReadOnlyDictionary<string, float> percents,
        IReadOnlyDictionary<string, Dictionary<string, int>> progress)
    {
        _unlocked = unlocked;
        _percents = percents;
        _progress = progress;

        UpdateProgress();

        if (_selectedProto != null)
            ShowDetailView(_selectedProto);
        else
            RebuildList();
    }

    private void CycleSort()
    {
        var values = (AchievementSortMode[])Enum.GetValues(typeof(AchievementSortMode));
        var nextIndex = ((int)_currentSort + 1) % values.Length;
        _currentSort = (AchievementSortMode)nextIndex;

        UpdateSortButtonText();
        RebuildList();
    }

    private void UpdateSortButtonText()
    {
        SortCycleButton.Text = Loc.GetString(
            "achievement-sorting-mode", ("mode", _currentSort.ToString().ToLower()));
    }

    private void UpdateProgress()
    {
        var allVisible = _proto.EnumeratePrototypes<AchievementPrototype>()
            .Where(p => !p.Hidden || _unlocked.Contains(p.ID))
            .ToList();

        var total = allVisible.Count;
        var done  = allVisible.Count(p => _unlocked.Contains(p.ID));

        ProgressLabel.Text = $"{done} / {total}";
        _progressPct = total > 0 ? done * 100f / total : 0f;
        ProgressPercentLabel.Text = $"{_progressPct:F0}%";

        UpdateProgressBar();
    }

    private void UpdateProgressBar()
    {
        var barWidth = ProgressBarBg.Size.X;
        if (barWidth <= 0)
            return;

        ProgressBarFill.MinWidth = (int)(barWidth * (_progressPct / 100f));
    }


    private void RebuildList()
    {
        _selectedProto = null;
        AchievementList.RemoveAllChildren();

        var search = SearchBox.Text.Trim().ToLowerInvariant();
        var protos = _proto.EnumeratePrototypes<AchievementPrototype>().ToList();

        IEnumerable<AchievementPrototype> sorted = _currentSort switch
        {
            AchievementSortMode.Unlocked => protos
                .OrderByDescending(p => _unlocked.Contains(p.ID))
                .ThenBy(p => Loc.GetString(p.Name ?? p.ID)),

            AchievementSortMode.Difficulty => protos
                .OrderBy(p => _percents.GetValueOrDefault(p.ID, 0f))
                .ThenBy(p => Loc.GetString(p.Name ?? p.ID)),

            _ => protos.OrderBy(p => Loc.GetString(p.Name ?? p.ID))
        };

        foreach (var proto in sorted)
        {
            var isUnlocked = _unlocked.Contains(proto.ID);
            var isHidden = proto.Hidden && !isUnlocked;

            if (!string.IsNullOrEmpty(search))
            {
                if (isHidden)
                    continue;

                var nameMatch = Loc.GetString(proto.Name ?? proto.ID).ToLowerInvariant().Contains(search);
                var descMatch = Loc.GetString(proto.Description ?? string.Empty).ToLowerInvariant().Contains(search);
                if (!nameMatch && !descMatch)
                    continue;
            }

            _progress.TryGetValue(proto.ID, out var condProgress);

            var percent = _percents.GetValueOrDefault(proto.ID, 0f);
            var entry = new AchievementEntry(proto, isUnlocked, percent, _proto, _cache, _spriteSystem, condProgress);
            entry.OnSelected += ShowDetailView;
            AchievementList.AddChild(entry);
        }
    }

    private void ShowDetailView(AchievementPrototype proto)
    {
        _selectedProto = proto;
        AchievementList.RemoveAllChildren();

        var backButton = new Button { Text = Loc.GetString("achievement-detail-back"), HorizontalAlignment = Robust.Client.UserInterface.Control.HAlignment.Left };
        var backStyle  = new StyleBoxFlat
        {
            BackgroundColor = Color.FromHex("#1a130dee"),
            BorderColor     = Color.FromHex("#5c4a3d"),
            BorderThickness = new Thickness(1),
        };

        backStyle.SetContentMarginOverride(StyleBox.Margin.Left,   12);
        backStyle.SetContentMarginOverride(StyleBox.Margin.Right,  12);
        backStyle.SetContentMarginOverride(StyleBox.Margin.Top,    6);
        backStyle.SetContentMarginOverride(StyleBox.Margin.Bottom, 6);
        backButton.StyleBoxOverride = backStyle;
        backButton.OnPressed += _ => RebuildList();
        AchievementList.AddChild(backButton);

        var isUnlocked = _unlocked.Contains(proto.ID);
        var percent = _percents.GetValueOrDefault(proto.ID, 0f);
        _progress.TryGetValue(proto.ID, out var condProgress);

        var entry = new AchievementEntry(proto, isUnlocked, percent, _proto, _cache, _spriteSystem, condProgress);
        AchievementList.AddChild(entry);

        AchievementList.AddChild(MakeSeparator());

        if (proto.Conditions is { Count: > 0 })
        {
            var conditionsHeader = new Label
            {
                Text = Loc.GetString("achievement-detail-conditions-header"),
                FontColorOverride = Color.FromHex("#8b6914"),
            };
            AchievementList.AddChild(conditionsHeader);

            foreach (var condition in proto.Conditions)
            {
                var condMsg = condition.GetDescription(_proto);
                var condLabel = new RichTextLabel { HorizontalExpand = true };
                condLabel.SetMessage(condMsg);

                var condPanel = new PanelContainer { HorizontalExpand = true };
                var condStyle = new StyleBoxFlat
                {
                    BackgroundColor = Color.FromHex("#160f08cc"),
                    BorderColor = Color.FromHex("#3d2e1e"),
                    BorderThickness = new Thickness(0, 0, 0, 1),
                };
                condStyle.SetContentMarginOverride(StyleBox.Margin.Left,   12);
                condStyle.SetContentMarginOverride(StyleBox.Margin.Right,  12);
                condStyle.SetContentMarginOverride(StyleBox.Margin.Top,    6);
                condStyle.SetContentMarginOverride(StyleBox.Margin.Bottom, 6);
                condPanel.PanelOverride = condStyle;
                condPanel.AddChild(condLabel);

                AchievementList.AddChild(condPanel);
            }
        }
    }

    private static PanelContainer MakeSeparator()
    {
        var sep = new PanelContainer { HorizontalExpand = true, SetHeight = 2 };
        sep.PanelOverride = new StyleBoxFlat { BackgroundColor = Color.FromHex("#3d2e1e") };
        return sep;
    }
}
