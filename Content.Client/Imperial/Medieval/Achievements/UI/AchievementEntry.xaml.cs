using Content.Shared.Imperial.Medieval.Achievements;
using Robust.Client.AutoGenerated;
using Robust.Client.GameObjects;
using Robust.Client.Graphics;
using Robust.Client.ResourceManagement;
using Robust.Client.UserInterface;
using Robust.Client.UserInterface.Controls;
using Robust.Client.UserInterface.XAML;
using Robust.Shared.Input;
using Robust.Shared.Prototypes;
using Robust.Shared.Utility;

namespace Content.Client.Imperial.Medieval.Achievements.UI;

[GenerateTypedNameReferences]
public sealed partial class AchievementEntry : PanelContainer
{
    public event Action<AchievementPrototype>? OnSelected;
    private AchievementPrototype _proto = default!;
    private bool _isInteractable;

    public AchievementEntry(
        AchievementPrototype proto,
        bool unlocked,
        float globalPercent,
        IPrototypeManager protoManager,
        IResourceCache cache,
        SpriteSystem spriteSystem,
        Dictionary<string, int>? progress = null)
    {
        RobustXamlLoader.Load(this);
        _proto = proto;

        _isInteractable = !proto.Hidden || unlocked;
        
        MouseFilter = _isInteractable ? MouseFilterMode.Stop : MouseFilterMode.Ignore;

        var rarity = AchievementRarityHelper.Resolve(proto, globalPercent, protoManager);
        ApplyRarityStripe(rarity);

        if (unlocked)
            SetUnlocked(proto, rarity, globalPercent, cache, spriteSystem);
        else if (proto.Hidden)
            SetHidden(proto);
        else
            SetLocked(proto, rarity, globalPercent);

        if (!unlocked && !proto.Hidden && proto.Conditions != null)
            ApplyProgress(proto, progress ?? new Dictionary<string, int>(), protoManager);
    }

    protected override void KeyBindDown(GUIBoundKeyEventArgs args)
    {
        base.KeyBindDown(args);

        if (_isInteractable && args.Function == EngineKeyFunctions.UIClick)
        {
            OnSelected?.Invoke(_proto);
            args.Handle();
        }
    }

    private void SetUnlocked(AchievementPrototype proto, AchievementRarityPrototype rarity, float percent, IResourceCache cache, SpriteSystem spriteSystem)
    {
        AchievementIcon.Texture = spriteSystem.Frame0(proto.Icon);
        AchievementIcon.Visible = true;
        QuestionMarkLabel.Visible = false;
        NameLabel.Text = Loc.GetString(proto.Name ?? proto.ID);
        NameLabel.FontColorOverride = Color.FromHex("#d3bc8e");

        var desc = new FormattedMessage();
        desc.PushColor(Color.FromHex("#9e8c78"));
        desc.AddText(proto.Description ?? string.Empty);
        desc.Pop();
        DescriptionLabel.SetMessage(desc);
        
        ApplyBackground(unlocked: true);
        ApplyRarityBadge(rarity, percent, visible: true);
    }

    private void SetLocked(AchievementPrototype proto, AchievementRarityPrototype rarity, float percent)
    {
        AchievementIcon.Visible = false;
        QuestionMarkLabel.Visible = true;
        NameLabel.Text = Loc.GetString(proto.Name ?? proto.ID);
        NameLabel.FontColorOverride = Color.FromHex("#4a3828");

        var desc = new FormattedMessage();
        desc.PushColor(Color.FromHex("#2e2218"));
        desc.AddText(BlurText(proto.Description ?? string.Empty));
        desc.Pop();
        DescriptionLabel.SetMessage(desc);

        ApplyBackground(unlocked: false);
        ApplyRarityBadge(rarity, percent, visible: true);
    }

    private void SetHidden(AchievementPrototype proto)
    {
        AchievementIcon.Visible = false;
        QuestionMarkLabel.Visible = true;
        NameLabel.Text = BlurText(Loc.GetString(proto.Name ?? proto.ID));
        NameLabel.FontColorOverride = Color.FromHex("#32251a");

        var desc = new FormattedMessage();
        desc.PushColor(Color.FromHex("#251b12"));
        desc.AddText(BlurText(Loc.GetString(proto.Description ?? string.Empty)));
        desc.Pop();
        DescriptionLabel.SetMessage(desc);

        ApplyBackground(unlocked: false);
        RarityBadge.Visible = false;
        PercentLabel.Visible = false;
    }

    private void ApplyProgress(
        AchievementPrototype proto,
        Dictionary<string, int> progress,
        IPrototypeManager protoManager)
    {
        if (ProgressContainer == null)
            return;

        ProgressContainer.RemoveAllChildren();
        
        ProgressContainer.Visible = false;

        foreach (var condition in proto.Conditions)
        {
            string? key = GetProgressKey(condition);
            int required = GetRequiredCount(condition);

            if (key == null || required <= 1)
                continue;

            ProgressContainer.Visible = true;

            var current = progress.GetValueOrDefault(key, 0);
            var pct = Math.Clamp((float)current / required, 0f, 1f);

            var condLabel = new Label
            {
                FontColorOverride = Color.FromHex("#7b6a58"),
                Text = $"{condition.GetDescription(protoManager).ToString().Split('\n')[0]}: {current} / {required}"
            };

            var barBg = new PanelContainer
            {
                HorizontalExpand = true,
                SetHeight = 5,
            };
            barBg.PanelOverride = new StyleBoxFlat { BackgroundColor = Color.FromHex("#1a1209") };

            var barFill = new PanelContainer
            {
                HorizontalAlignment = Control.HAlignment.Left,
                MinHeight = 5,
            };
            barFill.PanelOverride = new StyleBoxFlat
            {
                BackgroundColor = pct >= 1f
                    ? Color.FromHex("#5c8a3e")
                    : Color.FromHex("#8b6914")
            };
            barBg.AddChild(barFill);

            barBg.OnResized += () =>
            {
                barFill.MinWidth = (int)(barBg.Size.X * pct);
            };

            ProgressContainer.AddChild(condLabel);
            ProgressContainer.AddChild(barBg);
        }
    }

    private static string? GetProgressKey(AchievementCondition c) => c switch
    {
        KillMobCondition k => k.ProgressKey,
        LocationCondition l => l.ProgressKey,
        CollectAllItemsCondition ca => ca.ProgressKey,
        CollectAnyItemsCondition cn => cn.ProgressKey,
        _ => null
    };

    private static int GetRequiredCount(AchievementCondition c) => c switch
    {
        KillMobCondition k => k.RequiredCount,
        LocationCondition l => l.RequiredCount,
        CollectAnyItemsCondition cn => cn.RequiredCount,
        _ => 1
    };

    private void ApplyRarityStripe(AchievementRarityPrototype rarity)
    {
        RarityStripe.PanelOverride = new StyleBoxFlat { BackgroundColor = Color.FromHex(rarity.AccentColor) };
    }

    private void ApplyBackground(bool unlocked)
    {
        BackgroundPanel.PanelOverride = new StyleBoxFlat
        {
            BackgroundColor = Color.FromHex(unlocked ? "#231a10ee" : "#160f08dd"),
            BorderColor     = Color.FromHex("#3d2e1e"),
            BorderThickness = new Thickness(2, 1, 1, 1),
        };
    }

    private void ApplyRarityBadge(AchievementRarityPrototype rarity, float percent, bool visible)
    {
        RarityBadge.Visible = visible;
        PercentLabel.Visible = visible;

        if (!visible)
            return;

        RarityLabel.Text = Loc.GetString(rarity.Name);
        RarityLabel.FontColorOverride = Color.FromHex(rarity.AccentColor);
        PercentLabel.Text = $"{percent:F1}% {Loc.GetString("achievement-percent-players")}";
    }

    private static string BlurText(string text)
    {
        var chars = text.ToCharArray();
        for (var i = 0; i < chars.Length; i++)
        {
            if (!char.IsWhiteSpace(chars[i]))
                chars[i] = '█';
        }
        return new string(chars);
    }
}
