namespace Content.Client.Imperial.Medieval.MagicScroll;

[GenerateTypedNameReferences]
public sealed partial class MinesweeperWindow : DefaultWindow
{
    private readonly Random _random = new();
    private bool[,] _mineField = new bool[0, 0];
    private bool[,] _revealed = new bool[0, 0];
    private bool[,] _flagged = new bool[0, 0];
    private int _mineCount = 0;
    private int _revealedCount = 0;
    private bool _gameOver = false;
    private MagicRune _currentRune;
    private IGameTiming _gameTiming;
    private TimeSpan? _gameEndTime;
    private bool _hintUsed = false;
    private int _playerIntelligence = 10;

    private int _gridSize = 5;
    private int _totalMines = 2;
    private const float GameEndDelay = 3.0f;

    public event Action<bool, bool>? GameCompleted;

    public MinesweeperWindow()
    {
        RobustXamlLoader.Load(this);
        _gameTiming = IoCManager.Resolve<IGameTiming>();

        CloseButto.OnPressed += _ => Close();

        OnClose += () =>
        {
            if (!_gameOver)
                GameCompleted?.Invoke(false, false);
        };
        HintButton.OnPressed += _ =>
        {
            if (_hintUsed || _gameOver)
                return;

            UseHint();
        };
        RestartButton.OnPressed += _ =>
        {
            if (_gameOver)
                return;

            InitializeGame();
        };
    }

    private void UseHint()
    {
        for (int i = 0; i < _gridSize; i++)
        {
            for (int j = 0; j < _gridSize; j++)
            {
                if (!_revealed[i, j] && !_mineField[i, j] && !_flagged[i, j])
                {
                    RevealCell(i, j);
                    _hintUsed = true;
                    HintButton.Disabled = true;
                    StatusLabel.Text = "Подсказка использована!";
                    return;
                }
            }
        }

        StatusLabel.Text = "Нет доступных безопасных клеток для подсказки.";
        _hintUsed = true;
        HintButton.Disabled = true;
    }

    protected override void FrameUpdate(FrameEventArgs args)
    {
        base.FrameUpdate(args);

        if (_gameEndTime.HasValue && _gameTiming.CurTime >= _gameEndTime.Value)
        {
            _gameEndTime = null;
            Close();
        }
    }

    public void StartGame(MagicRune rune, int playerIntelligence, int gridSize, int totalMines)
    {
        _currentRune = rune;
        _playerIntelligence = playerIntelligence;
        _gridSize = gridSize;
        _totalMines = totalMines;
        RuneLabel.Text = $"Расшифровка руны: {MagicRuneData.GetSymbol(rune)} ({MagicRuneData.GetMeaning(rune)})";
        InitializeGame();
    }

    private void InitializeGame()
    {
        _mineField = new bool[_gridSize, _gridSize];
        _revealed = new bool[_gridSize, _gridSize];
        _flagged = new bool[_gridSize, _gridSize];
        _mineCount = _totalMines;
        _revealedCount = 0;
        _gameOver = false;
        _gameEndTime = null;
        _hintUsed = false;
        HintButton.Disabled = false;

        if (_playerIntelligence <= 15) HintButton.Disabled = true;

        var positions = new List<(int, int)>();
        for (int i = 0; i < _gridSize; i++)
        {
            for (int j = 0; j < _gridSize; j++)
            {
                positions.Add((i, j));
            }
        }

        for (int i = 0; i < positions.Count; i++)
        {
            var randomIndex = _random.Next(i, positions.Count);
            (positions[i], positions[randomIndex]) = (positions[randomIndex], positions[i]);
        }

        for (int i = 0; i < _mineCount; i++)
        {
            var (x, y) = positions[i];
            _mineField[x, y] = true;
        }

        CreateGrid();
        UpdateStatus();
    }

    private void CreateGrid()
    {
        MinesweeperGrid.RemoveAllChildren();

        MinesweeperGrid.Columns = _gridSize;

        for (int i = 0; i < _gridSize; i++)
        {
            for (int j = 0; j < _gridSize; j++)
            {
                var button = new Button
                {
                    Text = "",
                    MinSize = new Vector2(40, 40),
                };
                button.EnableAllKeybinds = true;

                var x = i;
                var y = j;

                button.OnPressed += args =>
                {
                    if (_gameOver) return;

                    if (args.Event.Function == EngineKeyFunctions.UIClick)
                    {
                        if (_revealedCount == 0)
                        {
                            while (true)
                            {
                                if (!_mineField[x, y] && CountAdjacentMines(x, y) == 0) break;
                                else InitializeGame();
                            }
                        }

                        HandleCellClick(x, y);
                    }
                    else if (args.Event.Function == EngineKeyFunctions.UIRightClick)
                    {
                        ToggleFlag(x, y);
                    }
                };

                MinesweeperGrid.AddChild(button);
            }
        }
    }

    private void HandleCellClick(int x, int y)
    {
        var targetX = x;
        var targetY = y;

        if (_playerIntelligence < 10)
        {
            var intelligenceDeficit = 10 - _playerIntelligence;
            var misfireChance = intelligenceDeficit * 5;

            if (_random.Next(100) < misfireChance)
            {
                var neighbors = GetNeighbors(x, y);
                if (neighbors.Count > 0)
                {
                    var randomNeighbor = neighbors[_random.Next(neighbors.Count)];
                    targetX = randomNeighbor.x;
                    targetY = randomNeighbor.y;

                    StatusLabel.Text = "Промах! Нажали не на ту клетку из-за низкого уровня интеллекта!";
                    StatusLabel.Modulate = Color.Orange;
                }
            }
        }

        RevealCell(targetX, targetY);
    }

    private List<(int x, int y)> GetNeighbors(int x, int y)
    {
        var neighbors = new List<(int, int)>();

        for (int dx = -1; dx <= 1; dx++)
        {
            for (int dy = -1; dy <= 1; dy++)
            {
                if (dx == 0 && dy == 0) continue;

                var nx = x + dx;
                var ny = y + dy;

                if (nx >= 0 && nx < _gridSize && ny >= 0 && ny < _gridSize)
                {
                    neighbors.Add((nx, ny));
                }
            }
        }

        return neighbors;
    }

    private void RevealCell(int x, int y)
    {
        if (_gameOver || _revealed[x, y] || _flagged[x, y]) return;

        _revealed[x, y] = true;
        _revealedCount++;

        var button = GetButtonAt(x, y);
        if (button == null) return;

        if (_mineField[x, y])
        {
            button.Text = "💣";
            button.Modulate = Color.Red;
            EndGame(false);
        }
        else
        {
            var adjacentMines = CountAdjacentMines(x, y);

            if (adjacentMines > 0)
            {
                button.Text = adjacentMines.ToString();
                button.Modulate = GetNumberColor(adjacentMines);
            }
            else
            {
                button.Text = "";
                button.Modulate = Color.LightGreen;

                for (int dx = -1; dx <= 1; dx++)
                {
                    for (int dy = -1; dy <= 1; dy++)
                    {
                        var nx = x + dx;
                        var ny = y + dy;

                        if (nx >= 0 && nx < _gridSize && ny >= 0 && ny < _gridSize)
                        {
                            if (!_revealed[nx, ny] && !_flagged[nx, ny])
                            {
                                RevealCell(nx, ny);
                            }
                        }
                    }
                }
            }

            if (_revealedCount == _gridSize * _gridSize - _mineCount)
            {
                EndGame(true);
            }
        }

        UpdateStatus();
    }

    private void ToggleFlag(int x, int y)
    {
        if (_gameOver || _revealed[x, y]) return;

        _flagged[x, y] = !_flagged[x, y];

        var button = GetButtonAt(x, y);
        if (button == null) return;

        if (_flagged[x, y])
        {
            button.Text = "!";
            button.Modulate = Color.Yellow;
        }
        else
        {
            button.Text = "";
            button.Modulate = Color.White;
        }

        UpdateStatus();
    }

    private Button? GetButtonAt(int x, int y)
    {
        var index = x * _gridSize + y;
        if (index < 0 || index >= MinesweeperGrid.ChildCount) return null;

        return MinesweeperGrid.GetChild(index) as Button;
    }

    private int CountAdjacentMines(int x, int y)
    {
        int count = 0;

        for (int dx = -1; dx <= 1; dx++)
        {
            for (int dy = -1; dy <= 1; dy++)
            {
                if (dx == 0 && dy == 0) continue;

                var nx = x + dx;
                var ny = y + dy;

                if (nx >= 0 && nx < _gridSize && ny >= 0 && ny < _gridSize)
                {
                    if (_mineField[nx, ny]) count++;
                }
            }
        }

        return count;
    }

    private Color GetNumberColor(int number)
    {
        return number switch
        {
            1 => Color.Blue,
            2 => Color.Green,
            3 => Color.Red,
            4 => Color.Purple,
            5 => Color.Maroon,
            _ => Color.White
        };
    }

    private void EndGame(bool won)
    {
        _gameOver = true;

        if (won)
        {
            _revealedCount = _gridSize * _gridSize - _mineCount;

            StatusLabel.Text = "Победа! Руна расшифрована! (Окно закроется через 3 секунды)";
            StatusLabel.Modulate = Color.Green;
            GameCompleted?.Invoke(true, false);
        }
        else
        {
            StatusLabel.Text = "Поражение! Вы наступили на мину! (Окно закроется через 3 секунды)";
            StatusLabel.Modulate = Color.Red;

            for (int i = 0; i < _gridSize; i++)
            {
                for (int j = 0; j < _gridSize; j++)
                {
                    if (_mineField[i, j])
                    {
                        var button = GetButtonAt(i, j);
                        if (button != null && !_revealed[i, j])
                        {
                            button.Text = "💣";
                            button.Modulate = Color.DarkRed;
                        }
                    }
                }
            }

            GameCompleted?.Invoke(false, true);
        }

        UpdateStatus();
        _gameEndTime = _gameTiming.CurTime + TimeSpan.FromSeconds(GameEndDelay);
    }

    private void UpdateStatus()
    {
        if (_gameOver)
        {
            var totalSafeCells = _gridSize * _gridSize - _mineCount;
            OpenedLabel.Text = $"Открыто: {totalSafeCells}/{totalSafeCells}";
            return;
        }

        var flaggedCount = 0;
        for (int i = 0; i < _gridSize; i++)
        {
            for (int j = 0; j < _gridSize; j++)
            {
                if (_flagged[i, j]) flaggedCount++;
            }
        }

        MinesLabel.Text = $"Мины: {_mineCount - flaggedCount}";
        OpenedLabel.Text = $"Открыто: {_revealedCount}/{_gridSize * _gridSize - _mineCount}";

        if (_playerIntelligence < 10)
        {
            var intelligenceDeficit = 10 - _playerIntelligence;
            var misfireChance = intelligenceDeficit * 5;
            StatusLabel.Text = $"Найдите все безопасные клетки! ВНИМАНИЕ: Шанс промаха {misfireChance}%. Слишком низкий уровень интеллекта!";
            StatusLabel.Modulate = Color.Yellow;
        }
        else
        {
            StatusLabel.Text = "Найдите все безопасные клетки!";
            StatusLabel.Modulate = Color.White;
        }
    }
}
