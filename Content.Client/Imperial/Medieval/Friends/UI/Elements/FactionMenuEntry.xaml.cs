using System.Linq;
using Content.Client.Friends;
using Content.Shared.Friends;
using Content.Shared.Friends.Components;
using Robust.Client.AutoGenerated;
using Robust.Client.Player;
using Robust.Client.UserInterface;
using Robust.Client.UserInterface.Controls;
using Robust.Client.UserInterface.XAML;

namespace Content.Client.Imperial.Medieval.Friends.UI.Elements;

/// <summary>
/// Панель, содержащая всю необходимую информацию о члене фракции
/// </summary>
[GenerateTypedNameReferences]
public sealed partial class FactionMenuEntry : Control
{
    public Action<int, string>? ObjectiveSet;
    public Action<int, FactionMemberGroup>? GroupSet;
    public Action<int>? RemoveButtonPressed;
    public Action<int, bool>? SetLeaderPressed;
    public bool Alive = true;

    public FactionMenuEntry(int ent, int self, FactionMemberData data, FactionMenuAccess access, string objective)
    {
        RobustXamlLoader.Load(this);
        IoCManager.InjectDependencies(this);

        NameLabel.SetMessage($"{data.JobPrefix} {data.Name}");
        RemoveButton.Visible = access == FactionMenuAccess.Full && ent != self;
        YouText.Visible = ent == self;

        if (data.Dead)
        {
            Alive = false;
            MainPanel.BackgroundPanelColor = Color.FromHex("#291e1e");
            FireText.SetMessage("Вычеркнуть");
        }

        if (data.Group == FactionMemberGroup.None)
        {
            for (var i = 0; i < FriendsSystem.GroupColors.Count; i++)
            {
                if (FriendsSystem.GroupColors.ElementAt(i).Key == FactionMemberGroup.None)
                    continue;

                var item = FriendsSystem.GroupColors.ElementAt(i);
                GroupSelector.AddItem(Loc.GetString($"group-{item.Key}"), item.Value, i);
            }
            GroupSelector.Select(0);
            Objective.Visible = false;
            GroupSelector.OnItemSelected += args => GroupSet?.Invoke(ent, FriendsSystem.GroupColors.ElementAt(args.Id).Key);
            GroupSelector.Visible = access == FactionMenuAccess.Full;
        }
        else
        {
            // Переключение видимости элементов
            GroupRemovePanel.Visible = true;
            GroupSelector.Visible = false;
            Objective.Visible = objective != string.Empty;

            Objective.Text = objective;

            // Кнопка исключения из группы
            GroupRemoveButtonText.Text = Loc.GetString($"group-{data.Group}");
            GroupRemoveButton.ModulateSelfOverride = FriendsSystem.GroupColors[data.Group];
            GroupRemoveButton.OnPressed += args => GroupSet?.Invoke(ent, FactionMemberGroup.None);

            // Доступ
            switch (access)
            {
                case FactionMenuAccess.Full:
                    {
                        GroupRemoveButton.Visible = true;
                        GroupRemoveCross.Visible = !data.Dead;
                        RemoveLeaderCross.Visible = data.Leader;
                        SetLeaderButton.Visible = ent != self;
                        LeaderPanel.Visible = ent != self;
                        LeaderPanel.BackgroundPanelColor = data.Leader ? Color.FromHex("#474747") : Color.FromHex("#2b2b2b");
                        SetLeaderButton.OnPressed += args => SetLeaderPressed?.Invoke(ent, !data.Leader);
                        SetLeaderButton.ToolTip = data.Leader ? "Снять с поста" : "Назначить лидером";
                        break;
                    }
                default:
                    {
                        GroupRemoveCross.Visible = false;
                        RemoveLeaderCross.Visible = false;
                        SetLeaderButton.Visible = false;
                        LeaderPanel.Visible = data.Leader;
                        GroupSelector.Visible = false;
                        break;
                    }
            }
        }

        RemoveButton.OnPressed += _ => RemoveButtonPressed?.Invoke(ent);
    }
}

public sealed class ColoredOptionButton : OptionButton
{
    private Color? _cachedColor;
    public void AddItem(string label, Color color, int? id = null)
    {
        _cachedColor = color;
        AddItem(label, id);
    }

    public override void ButtonOverride(Button button)
    {
        base.ButtonOverride(button);

        button.ModulateSelfOverride = _cachedColor;
        _cachedColor = null;
    }
}
