using Content.Shared.Friends;
using Robust.Client.AutoGenerated;
using Robust.Client.UserInterface.Controls;
using Robust.Client.UserInterface.XAML;

namespace Content.Client.Imperial.Medieval.Friends.UI.Elements;

/// <summary>
/// Панель для отображения членов фракции по их должности
/// </summary>
[GenerateTypedNameReferences]
public sealed partial class FactionJobPanel : BoxContainer
{
    public Action<FactionMemberGroup, string>? ObjectiveSet;

    public string Job;

    public FactionJobPanel(string job)
    {
        RobustXamlLoader.Load(this);

        Job = job;
        if (job != string.Empty)
            JobLabel.Text = job;
    }
}
